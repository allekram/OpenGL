    //配置opengl,开始深度测试
    glEnable(GL_DEPTH_TEST);

    //创建并编译着色器
    Shader lightingShader("Color.vs", "color.fs");
    Shader lightCubeShader("LightCube.vs", "LightCube.fs");

    //顶点数据 总共有6个面 x 2个三角形 x 3个顶点 = 36个顶点
    /*
        立方体的顶点数据按照逆时针顺序排列，
        以定义每个面的三角形顶点顺序。
        这样可以确保每个面的法线方向朝向立方体的外部，
        以便正确渲染光照效果。
    */
